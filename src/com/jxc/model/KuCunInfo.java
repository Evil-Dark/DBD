package com.jxc.model;

public class KuCunInfo {
	private String did;
	private int rks;
	private int xss;
	private int kcs;
	public String getDid() {
		return did;
	}
	public void setDid(String did) {
		this.did = did;
	}
	public int getRks() {
		return rks;
	}
	public void setRks(int rks) {
		this.rks = rks;
	}
	public int getXss() {
		return xss;
	}
	public void setXss(int xss) {
		this.xss = xss;
	}
	public int getKcs() {
		return kcs;
	}
	public void setKcs(int kcs) {
		this.kcs = kcs;
	}

	
}
